CREATE BIGFILE TABLESPACE &&1 DATAFILE 
  '+DATA01' SIZE 100M AUTOEXTEND ON NEXT 100M MAXSIZE UNLIMITED
NOLOGGING
DEFAULT 
  NO INMEMORY
ONLINE
EXTENT MANAGEMENT LOCAL AUTOALLOCATE
BLOCKSIZE 32K
SEGMENT SPACE MANAGEMENT AUTO
FLASHBACK ON;

