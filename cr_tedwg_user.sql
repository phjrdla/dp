  IDENTIFIED BY &2
  HTTP DIGEST DISABLE
  DEFAULT TABLESPACE &&1
  TEMPORARY TABLESPACE TEMP
  PROFILE USER_TECH
  ACCOUNT UNLOCK;

-- 2 Roles 
GRANT CONNECT TO &&1;
GRANT RESOURCE TO &&1;
ALTER USER &&1 DEFAULT ROLE ALL;

-- 1 System Privilege 
GRANT CREATE VIEW TO &&1;

-- 1 Tablespace Quota f
ALTER USER &&1 QUOTA UNLIMITED ON &&1;

